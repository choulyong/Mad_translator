# ═══════════════════════════════════════════════════════════════
# 배치 번역용 프롬프트 생성 함수
# ═══════════════════════════════════════════════════════════════
def get_translation_prompt(
    previous_speech_context: str = None,
    character_info: str = None,
    batch_number: int = None
) -> str:
    """자막 번역 프롬프트 생성

    Args:
        previous_speech_context: 이전 배치에서 확정된 말투 정보
        character_info: 등장 캐릭터 및 관계 정보
        batch_number: 현재 배치 번호

    Returns:
        완성된 번역 프롬프트
    """

    prompt = SUBTITLE_TRANSLATION_PROMPT

    # 이전 컨텍스트 삽입
    if previous_speech_context:
        prompt = prompt.replace(
            "{previous_speech_context}",
            previous_speech_context
        )
    else:
        prompt = prompt.replace(
            "{previous_speech_context}",
            "(첫 번째 배치 - 해당 없음)"
        )

    # 캐릭터 정보 삽입
    if character_info:
        prompt = prompt.replace(
            "{character_info}",
            character_info
        )
    else:
        prompt = prompt.replace(
            "{character_info}",
            "(자동 파악)"
        )

    # 배치 번호 표시
    if batch_number:
        prompt = f"[배치 #{batch_number}]\n\n" + prompt

    return prompt


# ═══════════════════════════════════════════════════════════════
# 간소화 버전 (토큰 절약)
# ═══════════════════════════════════════════════════════════════
COMPACT_TRANSLATION_PROMPT = """
🎬 자막 번역 핵심 규칙

【필수 규칙】
1. 자막 번호/타임코드 절대 변경 금지
2. 슬래시(/)로 줄바꿈 표시 금지 → 실제 줄바꿈 사용
3. 원문 의미 정확히 전달 (과도한 의역 금지)
4. 원문에 있는 내용 생략 금지, 없는 내용 추가 금지

【고유명사/이름】
• 캐릭터 이름: 첫 등장 시 정하고 일관되게 유지
• 지명/작품 고유 표현: 맥락에 맞게 통일

【포맷】
• 2줄 자막: 실제 줄바꿈(엔터)으로 구분
• 한 줄당 최대 18-20자 권장
• 의미 단위로 줄바꿈
"""


def get_compact_translation_prompt(previous_context: str = None) -> str:
    """토큰 절약용 간소화 프롬프트"""

    prompt = COMPACT_TRANSLATION_PROMPT

    if previous_context:
        prompt += f"\n\n【이전 배치 말투】\n{previous_context}\n위 말투 반드시 유지!"

    return prompt


# ═══════════════════════════════════════════════════════════════
# 배치 간 컨텍스트 추출용 후처리
# ═══════════════════════════════════════════════════════════════
CONTEXT_EXTRACTION_PROMPT = """
번역 완료 후, 마지막에 다음 형식으로 말투 요약을 추가하세요:

---
[확정된 말투]
• 닉 → 주디: 반말
• 주디 → 닉: 반말
• 주디 → 보고 국장: 존댓말
(이번 배치에서 새로 등장한 관계만)
---
"""


def get_prompt_with_context_request(base_prompt: str) -> str:
    """컨텍스트 추출 요청을 추가한 프롬프트"""
    return base_prompt + "\n\n" + CONTEXT_EXTRACTION_PROMPT


# ═══════════════════════════════════════════════════════════════
# 품질 검증용 체크 함수
# ═══════════════════════════════════════════════════════════════
def check_translation_quality(translated_srt: str) -> dict:
    """번역 품질 자동 체크

    Args:
        translated_srt: 번역된 SRT 텍스트

    Returns:
        문제점 딕셔너리
    """

    issues = {
        "slash_linebreaks": [],
        "translation_smell": [],
        "format_errors": []
    }

    lines = translated_srt.split('\n')

    for i, line in enumerate(lines):
        # 슬래시 줄바꿈 체크
        if ' / ' in line and not line.startswith('#'):
            issues["slash_linebreaks"].append({
                "line": i + 1,
                "content": line.strip()
            })

        # 번역투 체크
        translation_smells = [
            '할 것이다', '될 것이다', '일 것이다',
            '의 의 ', '에 대해서', '에 관해서',
            '그녀는 ', '그는 ', '그것은 '
        ]
        for smell in translation_smells:
            if smell in line:
                issues["translation_smell"].append({
                    "line": i + 1,
                    "pattern": smell,
                    "content": line.strip()
                })

    return issues


def print_quality_report(issues: dict):
    """품질 검사 결과 출력"""

    print("=" * 50)
    print("📋 번역 품질 검사 결과")
    print("=" * 50)

    if issues["slash_linebreaks"]:
        print(f"\n❌ 슬래시 줄바꿈 오류: {len(issues['slash_linebreaks'])}건")
        for item in issues["slash_linebreaks"][:5]:
            print(f"   Line {item['line']}: {item['content'][:50]}...")

    if issues["translation_smell"]:
        print(f"\n⚠️ 번역투 의심: {len(issues['translation_smell'])}건")
        for item in issues["translation_smell"][:5]:
            print(f"   Line {item['line']}: '{item['pattern']}' in {item['content'][:40]}...")

    if not any(issues.values()):
        print("\n✅ 문제 없음!")

    print("=" * 50)
