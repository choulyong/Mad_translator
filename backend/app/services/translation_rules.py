# ═══════════════════════════════════════════════════════════════
def get_translation_prompt(genre: str = "일반", include_deep_dive: bool = True) -> str:
    """장르에 맞는 전체 번역 규칙 프롬프트 반환

    Args:
        genre: 장르명 (한글 또는 영어)
        include_deep_dive: Deep-Dive 강화 규칙 포함 여부

    Returns:
        완성된 번역 프롬프트 문자열
    """

    # 장르 규칙 가져오기
    genre_lower = genre.lower() if genre else ""
    genre_specific = ""

    for key, value in GENRE_RULES.items():
        if key in genre_lower or genre_lower in key:
            genre_specific = value
            break

    # 영어 장르명 처리
    english_mapping = {
        "action": "액션",
        "romance": "로맨스",
        "comedy": "코미디",
        "horror": "공포",
        "thriller": "스릴러",
        "drama": "드라마",
        "sci-fi": "sf",
        "science fiction": "sf",
        "fantasy": "판타지",
        "animation": "애니메이션",
        "documentary": "다큐멘터리",
        "crime": "스릴러",
        "mystery": "스릴러"
    }

    for eng, kor in english_mapping.items():
        if eng in genre_lower:
            genre_specific = GENRE_RULES.get(kor, "")
            break

    if not genre_specific:
        genre_specific = """• 원문의 톤과 분위기 유지
• 캐릭터별 말투 일관성
• 자연스러운 한국어 구사
• 문화적 맥락 고려한 현지화"""

    genre_section = f"""
═══════════════════════════════════════
🎬 장르별 번역 전략 ({genre})
═══════════════════════════════════════
{genre_specific}
"""

    # 기본 프롬프트
    base_prompt = (
        BASIC_PRINCIPLES +
        HONORIFIC_RULES +
        AVOID_LITERAL +
        TYPO_CORRECTION +
        genre_section
    )

    # Deep-Dive 강화 규칙 추가
    if include_deep_dive:
        deep_dive_prompt = (
            "\n\n" + "═" * 70 + "\n" +
            "🔥 DEEP-DIVE 강화 규칙 (50개 배치 최적화) 🔥\n" +
            "═" * 70 + "\n\n" +
            BATCH_CONTEXT_RULES +
            SIMULTANEOUS_SPEECH +
            NONVERBAL_EXPRESSIONS +
            NUMBER_AND_UNIT_RULES +
            FOREIGN_LANGUAGE_RULES +
            ERROR_PREVENTION +
            QUALITY_CHECKLIST +
            UNTRANSLATABLE_HANDLING
        )
        return base_prompt + deep_dive_prompt

    return base_prompt


def get_batch_context_template() -> str:
    """배치 간 컨텍스트 전달 템플릿 반환"""
    return BATCH_CONTEXT_TEMPLATE


def get_quality_checklist() -> str:
    """품질 체크리스트만 반환"""
    return QUALITY_CHECKLIST


def get_error_prevention_guide() -> str:
    """오역 방지 가이드만 반환"""
    return ERROR_PREVENTION


