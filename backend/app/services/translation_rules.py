if __name__ == "__main__":
    # 기본 사용
    prompt = get_translation_prompt("액션")
    print(f"프롬프트 길이: {len(prompt)} 문자")

    # Deep-Dive 없이 기본만
    basic_prompt = get_translation_prompt("로맨스", include_deep_dive=False)
    print(f"기본 프롬프트 길이: {len(basic_prompt)} 문자")

    # 풀 버전
    full_prompt = get_translation_prompt("sf", include_deep_dive=True)
    print(f"풀 프롬프트 길이: {len(full_prompt)} 문자")
