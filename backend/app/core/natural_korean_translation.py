NATURAL_KOREAN_COMPACT = """
🇰🇷 자연스러운 한국어 번역 규칙:

[핵심] "한국 배우가 직접 말한다면 어떻게 말할까?"

【번역투 금지 패턴】
❌ "~할 것이다" → ✅ "~할 거야" / "~해"
❌ "~의 ~의" 연속 → ✅ 하나를 문장으로 풀기
❌ "그/그녀는" 과다 → ✅ 생략하거나 이름으로
❌ "~에 대해" 남발 → ✅ 대부분 생략
❌ "~하는 것" → ✅ "~하는 거" / "~하는 게"
❌ "~되어지다" 수동 → ✅ "~되다" / 능동으로

【구어체 변환】
~하였다 → ~했어 | ~것이다 → ~거야
~해야 한다 → ~해야 해 | ~할 수 없다 → ~못 해
그러나 → 근데 | 왜냐하면 → ~거든

【자막 길이】
• 1줄 최대 15-20자
• 길면 핵심만 압축

【직역 실수 금지】
What's up? → 뭐해? (뭐가 위야? ❌)
I got it → 알겠어 (그것을 얻었다 ❌)
No way! → 말도 안 돼! (방법이 없어 ❌)
Get out! → 나가! (밖으로 나가 ❌)

[체크] 소리 내어 읽었을 때 자연스러운가?
"""


