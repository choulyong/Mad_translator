TRANSLATION_SMELL_PATTERNS = """
═══════════════════════════════════════
🚫 번역투 금지 패턴
═══════════════════════════════════════

【패턴 1: "~할 것이다" 체】
❌ "나는 너를 죽일 것이다" → ✅ "널 죽여버릴 거야"
❌ "우리는 성공할 것이다" → ✅ "우린 해낼 거야"

【패턴 2: "~의 ~의" 연속 소유격】
❌ "그녀의 불편함의 원인" → ✅ "그녀가 불편한 이유"

【패턴 3: 불필요한 대명사】
❌ "그녀는 어디 있어?" → ✅ "어디 있어?"
❌ "나는 학교에 갈 것이다" → ✅ "학교 가야지"

【패턴 4: "~에 대해/~에 관해"】
❌ "이것에 대해 어떻게 생각해?" → ✅ "이거 어떻게 생각해?"

【패턴 5: "~하는 것" 명사화】
❌ "도망가는 것이 좋겠어" → ✅ "도망가는 게 좋겠어"

【패턴 6: 수동태 직역】
❌ "문이 열려졌다" → ✅ "문이 열렸다"

【패턴 7: "~로 인해/~에 의해"】
❌ "그의 실수로 인해" → ✅ "걔 실수 때문에"

【패턴 8: 조건절 직역】
❌ "만약 네가 간다면, 나도 갈 것이다" → ✅ "네가 가면 나도 갈게"
"""


