def get_compact_addon(character_map: str = None) -> str:
    """토큰 절약용 간소화 보충 프롬프트 (DeepDive Final 반영)"""

    prompt = """
【V4 보충 규칙 - 핵심만 (DeepDive Final)】

[존댓말 과다 지양]
• 부부/연인/친구/오랜 동료 → 반말이 기본
• 위기/긴박 상황 → 존댓말도 반말로 전환 가능
• 확실한 근거(직급 호칭, 격식 상황) 없으면 반말!

[서비스업/공식 예외 및 우선순위] ⭐
  1) 혼잣말(독백) → 무조건 반말 / 비꼬기(Sarcasm) → 상황에 따라 존댓말 허용
  2) 서비스(해요체)/공식(합쇼체) → 존댓말
  3) 경어 호칭(Sir/박사님) → 존댓말
  4) 캐릭터 관계도 → 지정 따름
  5) 이전 대화 말투 → 유지

[자막 경제성 + 길이 트리거]
• 짧을수록 좋다. 설명하지 말고 핵심만, 조사는 과감히 생략
• 20자 초과 시 반드시 축약 (긴박 장면 12자 내 목표)

[마침표 규칙]
• 반말 종결 시 마침표 생략 및 자동 제거
• 존댓말 종결 시 마침표 가능

[대명사/호칭/로컬라이징]
• you, I, he, she → 주어 생략이 1순위. 번역투 지양
• 성별이 모호한 3인칭 지칭은 '그 사람', 직책 등으로 중립적 번역
• 문화적 밈/고유명사 농담은 한국 정서에 맞게 의역

[짧은 대사]
• 1~2단어, 이름 호출, 감탄사, 욕설 누락 없이 반드시 번역

[V5.1 OTT 납품 규격]
• Dry Tone: 감정 과장 의역 지양, 정보 직관성·일관성 최우선
• 종결어미 통일: 한 캐릭터 반말→~어/아/지 통일, 존댓말→해요체 or 합쇼체 하나로
• 용어 사전(Glossary) 엄수: 전문용어 AI 임의 변환 금지
• 1줄 16자(±2), 최대 2줄(32자), 의미 단위로만 줄바꿈
"""

    if character_map:
        prompt += f"\n[캐릭터 관계도]\n{character_map}\n⚠️ 말투 원칙: 관계도 유지하되 예외(혼잣말/비꼬기) 및 우선순위를 따른다."

    return prompt


