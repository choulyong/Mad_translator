def get_addon_prompt(
    genre: str = None,
    character_map: str = None,
    enable_anti_honorific: bool = True,
    enable_service_formal: bool = True,
    enable_speech_priority: bool = True,
    enable_economy: bool = True,
    enable_length_trigger: bool = True,
    enable_punctuation: bool = True,
    enable_punctuation_post: bool = True,
    enable_emotional: bool = True,
    enable_pronoun: bool = True,
    enable_short_utterance: bool = True,
    enable_deep_polish: bool = True,
    enable_srt_format: bool = True,
    enable_content_type: bool = True,
    enable_ott_standard: bool = True,
    enable_speech_profile: bool = True,
) -> str:
    """V4 보충 프롬프트 생성 (DeepDive Final Edition + V5/V5.1 Update)"""

    parts = []
    parts.append("""
╔═══════════════════════════════════════════════════════════════════════════════╗
║  🎬 V4 보충 규칙 - 사람 번역 수준 달성을 위한 추가 규칙 (DeepDive Final)      ║
╚═══════════════════════════════════════════════════════════════════════════════╝
""")

    # V5: SRT 포맷 강제 - 최우선 배치
    if enable_srt_format:
        parts.append(SRT_FORMAT_RULES)

    if genre:
        parts.append(f"【장르】 {genre}\n")

    # V5: 콘텐츠 타입 스위칭 - 장르 직후 배치
    if enable_content_type:
        parts.append(CONTENT_TYPE_SWITCHING)

    if enable_anti_honorific:
        parts.append(ANTI_HONORIFIC_DEFAULT)

    if enable_service_formal:
        parts.append(SERVICE_FORMAL_RULES)

    if enable_speech_priority:
        parts.append(SPEECH_PRIORITY_RULES)

    if enable_economy:
        parts.append(SUBTITLE_ECONOMY)

    if enable_length_trigger:
        parts.append(SUBTITLE_LENGTH_TRIGGER)

    if enable_punctuation:
        parts.append(PUNCTUATION_RULES)

    if enable_punctuation_post:
        parts.append(PUNCTUATION_POST_RULES)

    if character_map:
        map_section = CHARACTER_MAP_TEMPLATE.replace("{character_map}", character_map)
        parts.append(map_section)

    if enable_emotional:
        parts.append(EMOTIONAL_RULES)

    if enable_pronoun:
        parts.append(PRONOUN_RULES)

    if enable_short_utterance:
        parts.append(SHORT_UTTERANCE_RULES)

    if enable_deep_polish:
        parts.append(DEEP_POLISH_PASS)

    if enable_ott_standard:
        parts.append(V5_1_OTT_STANDARD_RULES)

    # V6: 마스터 디렉팅 - V5.1 Dry Tone 위에 미세 연출 덧입히기
    parts.append(V6_MASTER_DIRECTING_RULES)

    # [보충 M]: 성격→말투 프로필 자동 생성
    if enable_speech_profile:
        parts.append(SPEECH_PROFILE_RULES)

    return "\n".join(parts)


