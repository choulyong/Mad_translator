def build_character_map(characters: list[dict]) -> str:
    """캐릭터 관계도 텍스트 생성"""
    lines = []
    lines.append("┌────────────────────────────────────────────────┐")
    lines.append("│  화자 → 상대         관계        말투          │")
    lines.append("├────────────────────────────────────────────────┤")

    for c in characters:
        fr = c.get("from", "?")
        to = c.get("to", "?")
        rel = c.get("relation", "?")
        sp = c.get("speech", "반말")
        marker = "🔴" if sp == "반말" else "🔵"
        lines.append(f"│  {fr} → {to:<10} {rel:<10} {marker} {sp:<6}   │")

    lines.append("└────────────────────────────────────────────────┘")
    return "\n".join(lines)


def build_character_map_from_personas(personas: str) -> str:
    """페르소나 문자열에서 캐릭터 관계도 자동 생성

    Args:
        personas: "캐릭터1: 설명, 캐릭터2: 설명" 형식의 문자열

    Returns:
        간단한 관계도 텍스트
    """
    if not personas:
        return ""

    import re
    names = re.findall(r'([가-힣a-zA-Z]+)\s*[:：]', personas)

    if len(names) < 2:
        return ""

    lines = ["【등장인물 간 기본 관계 추정】"]
    lines.append("⚠️ 아래는 추정입니다. 실제 대화 문맥에서 확정하세요.")

    for i, name in enumerate(names[:5]):  # 최대 5명
        lines.append(f"• {name}: 첫 대사에서 말투 확정 → 이후 유지")

    return "\n".join(lines)


def build_character_map_simple(map_text: str) -> str:
    """간단한 텍스트 형태의 관계도"""
    return map_text


