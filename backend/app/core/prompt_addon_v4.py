def get_full_prompt_v4(
    genre: str = None,
    character_map: str = None,
    previous_speech_context: str = None,
    batch_number: int = None,
    compact: bool = False,
) -> str:
    """V3 + V4 통합 프롬프트"""
    try:
        from balanced_subtitle_prompt import get_translation_prompt, get_compact_prompt
        if compact:
            base = get_compact_prompt(previous_speech_context)
        else:
            base = get_translation_prompt(previous_speech_context, None, batch_number)
    except ImportError:
        base = "【기본 자막 번역 규칙은 balanced_subtitle_prompt.py 참조】"

    if compact:
        addon = get_compact_addon(character_map)
    else:
        addon = get_addon_prompt(genre=genre, character_map=character_map)

    return base + "\n\n" + addon


