SPEECH_FEWSHOT_EXAMPLES = """
【예시: 올바른 일관성 유지】
┌─────────────────────────────────────────────────────────────┐
│ #5  A → B: "어디 가?" (반말 잠금 🔒)                       │
│ #20 A → B: "나도 같이 가도 돼?" ✅ (반말 유지)            │
│ #20 A → B: "저도 같이 가도 될까요?" ❌ (위반!)            │
│ #35 A → B: "나 기다려!" ✅ (반말 유지)                    │
└─────────────────────────────────────────────────────────────┘

【예시: 쌍방향 독립 추적】
┌─────────────────────────────────────────────────────────────┐
│ #8  A → B: "보고서 끝냈어?" (A→B: 반말 🔒)                │
│ #10 B → A: "네, 끝냈습니다." (B→A: 존댓말 🔒)            │
│ #25 A → B: "잘했어." ✅ (A→B 반말 유지)                   │
│ #30 B → A: "감사합니다." ✅ (B→A 존댓말 유지)            │
│ #30 B → A: "고마워." ❌ (위반!)                           │
└─────────────────────────────────────────────────────────────┘
"""


def get_speech_enforcement_for_translation(
    previous_context: str = None,
    use_compact: bool = True
) -> str:
    """번역 시 사용할 말투 일관성 프롬프트 생성

    Args:
        previous_context: 이전 배치에서 확정된 말투 정보
        use_compact: True면 컴팩트 버전 사용 (토큰 절약)

    Returns:
        말투 강제 프롬프트
    """
    parts = []

    # 1. 핵심 규칙
    if use_compact:
        parts.append(UNIVERSAL_SPEECH_COMPACT)
    else:
        parts.append(UNIVERSAL_SPEECH_FULL)

    # 2. 이전 컨텍스트
    if previous_context:
        parts.append(BATCH_CONTEXT_TEMPLATE.format(confirmed_speech=previous_context))

    return "\n".join(parts)


def get_speech_enforcement_for_strategy() -> str:
    """전략 기획서 생성 시 사용할 말투 프롬프트 (상세 버전)"""
    return UNIVERSAL_SPEECH_FULL + "\n" + SPEECH_FEWSHOT_EXAMPLES


def format_confirmed_speech(confirmed_speech_dict: dict) -> str:
    """확정된 말투 딕셔너리를 문자열로 포맷

    Args:
        confirmed_speech_dict: {(speaker, listener): {"level": "반말", "confirmed_at": "#5"}, ...}

    Returns:
        포맷된 문자열
    """
    if not confirmed_speech_dict:
        return ""

    lines = []
    for pair, info in confirmed_speech_dict.items():
        if isinstance(pair, tuple) and len(pair) == 2:
            speaker, listener = pair
            level = info.get('level', '미확정')
            confirmed_at = info.get('confirmed_at', '?')
            lines.append(f"• {speaker} → {listener}: {level} (#{confirmed_at}에서 확정)")

    return "\n".join(lines)
